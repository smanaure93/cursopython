intNumber = 30
floatNumber = 99.99

# Número entero
print(type(intNumber))
print(intNumber)

# Número flotante
print(type(floatNumber))
print(floatNumber)

# Volver flotante el entero
print(type(float(intNumber)))
print(float(intNumber))

# Volver entero el flotante
print(type(int(floatNumber)))
print(int(floatNumber))
