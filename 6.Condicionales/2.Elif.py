character = input("Ingresa una letra: ")

if character.lower() == "a":
    print("Esta vocal es la A")
elif character.lower() == "e":
    print("Esta vocal es la E")
elif character.lower() == "i":
    print('Esta vocal es la I')
elif character.lower() == "o":
    print("Esta vocal es la O")
elif character.lower() == "u":
    print("Esta vocal es la U")
else:
    print("No ingresaste una vocal")
